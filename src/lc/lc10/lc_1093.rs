// https://leetcode.com/problems/statistics-from-a-large-sample/
// 1093. Statistics from a Large Sample
pub struct Solution;
impl Solution {
    pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
        // min max mean median mode
        let mut sum = 0.0;
        let mut cnt = 0;
        let mut min = -1.0;
        let mut max = -1.0;
        let mut max_c = 0;
        let mut max_c_i = 0.0;
        for (i, &c) in count.iter().enumerate() {
            cnt += c;
            sum += i as f64 * c as f64;
            if min == -1.0 && c > 0 {
                min = i as f64;
            }
            if c > 0 {
                max = i as f64;
            }
            if c > max_c {
                max_c = c;
                max_c_i = i as f64;
            }
        }
        let mut mi = -1.0;
        let mut mj = 0.0;
        let cj = cnt / 2 + 1;
        let ci = if cnt % 2 == 0 { cj - 1 } else { cj };
        let mut cc = 0;
        for (i, &c) in count.iter().enumerate() {
            if mi == -1.0 && cc + c >= ci {
                mi = i as f64;
            }
            if cc + c >= cj {
                mj = i as f64;
                break;
            }
            cc += c;
        }
        vec![min, max, sum / cnt as f64, (mi + mj) / 2.0, max_c_i]
    }
}
