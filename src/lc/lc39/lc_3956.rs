// https://leetcode.com/problems/maximum-sum-of-m-non-overlapping-subarrays-i/
// 3956. Maximum Sum of M Non-Overlapping Subarrays I
pub struct Solution;
impl Solution {
    pub fn maximum_sum(nums: Vec<i32>, m: i32, l: i32, r: i32) -> i64 {
        #[derive(Clone, Copy)]
        struct State {
            value: i64,
            count: i32,
        }

        fn better(left: State, right: State) -> State {
            if left.value != right.value {
                if left.value > right.value {
                    left
                } else {
                    right
                }
            } else if left.count >= right.count {
                left
            } else {
                right
            }
        }

        fn evaluate(prefix: &[i64], l: usize, r: usize, penalty: i64) -> State {
            use std::collections::VecDeque;

            let n = prefix.len() - 1;
            let mut dp = vec![State { value: 0, count: 0 }; n + 1];
            let mut deque: VecDeque<(usize, State)> = VecDeque::new();

            for i in 1..=n {
                if i >= l {
                    let start = i - l;
                    let candidate = State {
                        value: dp[start].value - prefix[start],
                        count: dp[start].count,
                    };
                    while let Some(&(_, tail)) = deque.back() {
                        if better(candidate, tail).value == candidate.value
                            && better(candidate, tail).count == candidate.count
                        {
                            deque.pop_back();
                        } else {
                            break;
                        }
                    }
                    deque.push_back((start, candidate));
                }

                while let Some(&(start, _)) = deque.front() {
                    if start + r < i {
                        deque.pop_front();
                    } else {
                        break;
                    }
                }

                dp[i] = dp[i - 1];
                if let Some(&(_, base)) = deque.front() {
                    let take = State {
                        value: base.value + prefix[i] - penalty,
                        count: base.count + 1,
                    };
                    dp[i] = better(dp[i], take);
                }
            }

            dp[n]
        }

        let n = nums.len();
        let l = l as usize;
        let r = r as usize;
        let m = m as i32;

        let mut prefix = vec![0_i64; n + 1];
        for (index, num) in nums.into_iter().enumerate() {
            prefix[index + 1] = prefix[index] + num as i64;
        }

        let unconstrained = evaluate(&prefix, l, r, 0);
        if unconstrained.count >= 1 && unconstrained.count <= m {
            return unconstrained.value;
        }

        let target = if unconstrained.count == 0 { 1 } else { m };
        let mut low = -10_000_000_000_001_i64;
        let mut high = 10_000_000_000_001_i64;
        while low < high {
            let mid = low + (high - low + 1) / 2;
            if evaluate(&prefix, l, r, mid).count >= target {
                low = mid;
            } else {
                high = mid - 1;
            }
        }

        let best = evaluate(&prefix, l, r, low);
        best.value + low * target as i64
    }
}
